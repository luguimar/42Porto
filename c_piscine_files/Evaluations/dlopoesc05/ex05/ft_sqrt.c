#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while (i * i < nb && i <= 46340)
		{
			++i;
		}
		if (i * i == nb)
		{
			return (i);
		}
	}
	return (0);
}

int	main(void)
{
	int	nb;

	nb = 49;
	printf("%d\n", ft_sqrt(nb));
	return (0);
}

