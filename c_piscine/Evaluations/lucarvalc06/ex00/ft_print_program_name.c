#include <unistd.h>

int	main(int ag, char **av)
{
	int	i;

	i = 0;
	if (ag == 1)
	{
		while (av[0][i] != '\0')
		{
			write (1, &av[0][i], 1);
			i++;
		}
		write (1, "\n", 1);
	}
	return (0);
}
