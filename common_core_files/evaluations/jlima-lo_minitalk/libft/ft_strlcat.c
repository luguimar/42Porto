#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <string.h>
#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	l_dst;
	size_t	l_src;
	size_t	i;
	size_t	fin;

	l_dst = ft_strlen(dst);
	l_src = ft_strlen((char *)src);
	i = 0;
	fin = 0;
	if (size > l_dst)
		fin = l_dst + l_src;
	else
		fin = size + l_src;
	if (size > 0)
	{
		while (src[i] != '\0' && (l_dst < (size - 1)))
		{
			dst[l_dst] = src[i];
			i++;
			l_dst++;
		}
		dst[l_dst] = '\0';
	}
	return (fin);
}

