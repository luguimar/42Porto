#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	p;
	int		i;

	p = '\n';
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &p, 1);
}
